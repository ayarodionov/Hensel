#--------------------------------------------------------------------------------------------------
# Functions on [a,b]
#--------------------------------------------------------------------------------------------------
using Test
using Hensel

#--------------------------------------------------------------------------------------------------

@test Hensel.hVector(11.5, 11, 12, 2, 3)    == [1, 0, 0]
@test Hensel.hVector(0.0, -1.0, 1.0, 2, 3)  == [1, 0, 0]
@test Hensel.hVector(-0.9, -1.0, 1.0, 2, 3) == [0, 0, 0]
@test Hensel.hVector(100.3, 100, 101, 2, 3) == [0, 1, 0]


